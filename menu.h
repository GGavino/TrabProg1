void titulo(char titulo[]); // Função usada em todos os menus para gerar o seu titulo;

int menuini(); // É o primeiro menu que o usuario ve e o permite escolher oq quer gerenciar no momento: cliente, imoveis ou alugueis;

int menuimovel(); // O principal menu dos imoveis onde o usuario escolhe oq que fazer com eles(inserir um novo, alterar ou remover um imovel já existente etc);

int menuclientes(); // O principal menu dos cliente onde o usuario escolhe oq que fazer com eles(inserir um novo, alterar ou remover um imovel já existente etc);

int menuListarClientesOrdem(); // O menu onde o usuário escolhe a ordem de listagem;

int menuAlterarClientes();// O menu onde o usuário escolhe o que alterar;

int menuFiltrarClientes(); // O menu onde o usuário escolhe o tipo de filtragem;

int menuAluguel(); // O principal menu dos alugueis onde o usuario escolhe oq que fazer com eles(inserir um novo, verificar imoveis diponiveis etc);

int menuAlterarImovel(); // O menu onde o usuario escolhe qual informação do imovel deseja alterar;

int menuListarImoveisOrdem(); // O menu onde o usuario escolhe por qual ordem deseja que os imoveis sejam listados;

int menuFiltrarImoveis(); // O menu onde o usuario escolhe qual filtro que aplicar aos imoveis;
